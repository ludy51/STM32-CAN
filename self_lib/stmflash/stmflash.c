void stm_WriteFlash_NoCheck(u32 Write_Addr,u16 *Buffer,u16 Write_Num)
{
		u16 i;
	for(i=0;i<Write_Num;i++)
	{
		FLASH_ProgramHalfWord(Write_Addr,Buffer[i]);
	    Write_Addr+=2;//地址增加2.
	}

}



