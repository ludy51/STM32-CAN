u16 Read_Half_WORD(u32 flshaddr)
{
	return *(vu16*)flshaddr; 
}

void Stm_writeFlash(u32 Write_Addr,u16 *Buffer,u16 Write_Num)
{
	u32 secpos;	   //扇区地址
	u16 secoff;	   //扇区内偏移地址(16位字计算)
	u16 secremain; //扇区内剩余地址(16位字计算)	   
 	u16 i;    
	u32 offaddr;   //去掉0X08000000后的地址
	if(Write_Addr<STM32_FLASH_BASE||(Write_Addr>=(STM32_FLASH_BASE+1024*STM32_FLASH_SIZE)))return;//非法地址
	FLASH_Unlock();						//解锁
	offaddr=Write_Addr-STM32_FLASH_BASE;		//实际偏移地址.
	secpos=offaddr/STM_SECTOR_SIZE;			//扇区地址  0~127 for STM32F103RBT6
	secoff=(offaddr%STM_SECTOR_SIZE)/2;		//在扇区内的偏移(2个字节为基本单位.)
	secremain=STM_SECTOR_SIZE/2-secoff;		//扇区剩余空间大小   
	if(Write_Num<=secremain)secremain=Write_Num;//不大于该扇区范围
	while(1) 
	{	
		Stm_readFlash(secpos*STM_SECTOR_SIZE+STM32_FLASH_BASE,STMFLASH_BUF,STM_SECTOR_SIZE/2);//读出整个扇区的内容
		for(i=0;i<secremain;i++)//校验数据
		{
			if(STMFLASH_BUF[secoff+i]!=0XFFFF)break;//需要擦除  	  
		}
		if(i<secremain)//需要擦除
		{
			FLASH_ErasePage(secpos*STM_SECTOR_SIZE+STM32_FLASH_BASE);//擦除这个扇区
			for(i=0;i<secremain;i++)//复制
			{
				STMFLASH_BUF[i+secoff]=Buffer[i];	  
			}
			stm_WriteFlash_NoCheck(secpos*STM_SECTOR_SIZE+STM32_FLASH_BASE,STMFLASH_BUF,STM_SECTOR_SIZE/2);//写入整个扇区  
		}else stm_WriteFlash_NoCheck(Write_Addr,Buffer,secremain);//写已经擦除了的,直接写入扇区剩余区间. 				   
		if(Write_Num==secremain)break;//写入结束了
		else//写入未结束
		{
			secpos++;				//扇区地址增1
			secoff=0;				//偏移位置为0 	 
		   	Buffer+=secremain;  	//指针偏移
			Write_Addr+=secremain;	//写地址偏移	   
		   	Write_Num-=secremain;	//字节(16位)数递减
			if(Write_Num>(STM_SECTOR_SIZE/2))secremain=STM_SECTOR_SIZE/2;//下一个扇区还是写不完
			else secremain=Write_Num;//下一个扇区可以写完了
		}	 
	};	
	FLASH_Lock();//上锁
}


void Stm_readFlash(u32 Read_Addr,u16 *Buffer,u16 Read_Num)
{
	u16 i;
	for(i=0;i<Read_Num;i++)
	{
		Buffer[i]=Read_Half_WORD(Read_Addr);//读取2个字节.
		Read_Addr+=2;//偏移2个字节.	
	}
	
}







